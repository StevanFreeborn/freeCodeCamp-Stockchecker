const mongoose = require('mongoose');

const stockSchemaOptions = {
    timestamps: true,
}
const StockSchema = mongoose.Schema({
    stock: { type: String, trim: true},
    likes: [String],
    avgTotalVolume: { type: Number, },
    calculationPrice: { type: String, trim: true, },
    change: mongoose.Schema.Types.Decimal128,
    changePercent: mongoose.Schema.Types.Decimal128,
    close: mongoose.Schema.Types.Decimal128,
    closeSource: { type: String, trim: true, },
    closeTime: { type: Number, },
    companyName: { type: String, trim: true, },
    currency: { type: String, trim: true, },
    delayedPrice: mongoose.Schema.Types.Decimal128,
    delayedPriceTime: { type: Number, },
    extendedChange: mongoose.Schema.Types.Decimal128,
    extendedChangePercent: mongoose.Schema.Types.Decimal128,
    extendedPrice: mongoose.Schema.Types.Decimal128,
    extendedPriceTime: { type: Number, },
    high: mongoose.Schema.Types.Decimal128,
    highSource: { type: String, trim: true, },
    highTime: { type: Number, },
    iexAskPrice: mongoose.Schema.Types.Decimal128,
    iexAskSize: { type: Number, },
    iexBidPrice: mongoose.Schema.Types.Decimal128,
    iexBidSize: { type: Number, },
    iexClose: mongoose.Schema.Types.Decimal128,
    iexCloseTime: { type: Number, },
    iexLastUpdated: { type: Number, },
    iexMarketPercent: mongoose.Schema.Types.Decimal128,
    iexOpen: mongoose.Schema.Types.Decimal128,
    iexOpenTime: { type: Number, },
    iexRealtimePrice: mongoose.Schema.Types.Decimal128,
    iexRealtimeSize: { type: Number, },
    iexVolume: { type: Number, },
    lastTradeTime: { type: Number, },
    latestPrice: mongoose.Schema.Types.Decimal128,
    latestSource: { type: String, trim: true, },
    latestTime: { type: String, trim: true, },
    latestUpdate: { type: Number, },
    latestVolume: { type: Number, },
    low: mongoose.Schema.Types.Decimal128,
    lowSource: { type: String, trim: true, },
    lowTime: { type: Number, },
    marketCap: { type: Number, },
    oddLotDelayedPrice: mongoose.Schema.Types.Decimal128,
    oddLotDelayedPriceTime: { type: Number, },
    open: mongoose.Schema.Types.Decimal128,
    openTime: { type: Number, },
    openSource: { type: String, trim: true, },
    peRatio: mongoose.Schema.Types.Decimal128,
    previousClose: mongoose.Schema.Types.Decimal128,
    previousVolume: { type: Number, },
    primaryExchange: { type: String, trim: true, },
    symbol: { type: String, trim: true, },
    volume: { type: Number, },
    week52High: mongoose.Schema.Types.Decimal128,
    week52Low: mongoose.Schema.Types.Decimal128,
    ytdChange: mongoose.Schema.Types.Decimal128,
    isUSMarketOpen: { type: Boolean, },
}, stockSchemaOptions);

const Stock = mongoose.model('stocks', StockSchema);

module.exports = Stock;